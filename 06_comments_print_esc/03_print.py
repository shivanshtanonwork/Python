# sep specifies the separator between multiple items printed. By default, it is a space ' '. You can change it to any string you want.

# end specifies what to print at the end of the output. By default, it is a newline \n, which moves the cursor to the next line. You can change it to keep the output on the same line or add custom characters.


print("Hello World", "Shivansh", 26, sep=",")

print("Hello World", end="..")
print("Shivansh")

print("Hello","World", sep=", ", end="!\n")

