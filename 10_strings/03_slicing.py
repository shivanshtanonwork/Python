name = "Shivansh"

print(name[0:2]) #goes from 0 to 2-1 i.e 0 to 1

print(name[2:-1]) # Same as name[2:7]
# print(name[2:7])

# print(name[0:10:n]) # Skip n-1 characters
print(name[0:10:1]) #Skip 0 characters
print(name[0:10:2]) # Skip 1 characters

print(name[:7]) # Replace the empty no. with 0 # name[0:7]
print(name[1:]) # Replace the second empty no. with length #name [1:15]