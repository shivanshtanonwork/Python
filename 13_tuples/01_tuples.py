a = (3,4,5,6,7,8)
# Tuples they are immutable 
# We use Tuples when we don't want our collection to be changed
print(a)
# a[3] = 9 #This cannot be done as tuple values cannot be changed

# Parenthesis () in python is also used to group things together. So to create a tuple with single element we add , "comma" at the end. if we dont have comma then it wont create tuple with single element 
b = (15,)
print(b)
